/*
Question:
How can you create a Java program to demonstrate sqrt, pow, abs, ceil, floor, round, max, min, PI, and E with examples?
*/
public class MathDemo {
    public static void main(String[] args) {
        System.out.println("sqrt(49): " + Math.sqrt(49));
        System.out.println("pow(2, 5): " + Math.pow(2, 5));
        System.out.println("abs(-42): " + Math.abs(-42));
        System.out.println("ceil(7.2): " + Math.ceil(7.2));
        System.out.println("floor(7.9): " + Math.floor(7.9));
        System.out.println("round(7.5): " + Math.round(7.5));
        System.out.println("max(15, 30): " + Math.max(15, 30));
        System.out.println("min(15, 30): " + Math.min(15, 30));
        System.out.println("PI: " + Math.PI);
        System.out.println("E: " + Math.E);
    }
}
